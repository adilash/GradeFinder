public class idArray {

    private int id1;

    private int id2;

    private int id3;

    private int id4;

    public idArray(int id1, int id2, int id3, int id4) {
        this.id1 = id1;
        this.id2 = id2;
        this.id3 = id3;
        this.id4 = id4;
    }

    public int getId1() {
        return id1;
    }

    public int getId2() {
        return id2;
    }

    public int getId3() {
        return id3;
    }

    public int getId4() {
        return id4;
    }
}
